const translateViaLibre = async (endpoint: string, text: string, signal?: AbortSignal): Promise<string | null> => {
  const response = await fetch(endpoint, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      q: text,
      source: 'ar',
      target: 'en',
      format: 'text',
    }),
    signal,
  });

  if (!response.ok) return null;
  const data = await response.json().catch(() => null);
  const translatedText = data && typeof data.translatedText === 'string' ? data.translatedText : null;
  return translatedText && translatedText.trim() ? translatedText.trim() : null;
};

export const translateArToEn = async (text: string): Promise<string | null> => {
  const trimmed = text.trim();
  if (!trimmed) return null;

  const endpoints = [
    'https://libretranslate.de/translate',
    'https://translate.argosopentech.com/translate',
    'https://libretranslate.com/translate',
  ];

  for (const endpoint of endpoints) {
    const controller = new AbortController();
    const timeoutId = window.setTimeout(() => controller.abort(), 8000);
    try {
      const translated = await translateViaLibre(endpoint, trimmed, controller.signal);
      if (translated) return translated;
    } catch {
    } finally {
      window.clearTimeout(timeoutId);
    }
  }

  return null;
};

export const getLocalizedText = (
  value: { ar?: string; en?: string } | undefined | null
): string => {
  if (!value) return '';
  // Always return Arabic if available, falling back to English only if Arabic is missing
  return value.ar || value.en || '';
};
